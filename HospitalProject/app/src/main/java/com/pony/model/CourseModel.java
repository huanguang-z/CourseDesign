package com.pony.model;

import java.io.Serializable;

public class CourseModel implements Serializable{

	private String  courseLength;
	private String  courseTeacherName;
	private String  courseMessage;
	private String  courseTime;
	private String  courseMoney;
	private String  courseId;
	private String  courseNumber;
	private String  courseName;
	public String getCourseLength() {
		return courseLength;
	}
	public void setCourseLength(String courseLength) {
		this.courseLength = courseLength;
	}
	public String getCourseTeacherName() {
		return courseTeacherName;
	}
	public void setCourseTeacherName(String courseTeacherName) {
		this.courseTeacherName = courseTeacherName;
	}
	public String getCourseMessage() {
		return courseMessage;
	}
	public void setCourseMessage(String courseMessage) {
		this.courseMessage = courseMessage;
	}
	public String getCourseTime() {
		return courseTime;
	}
	public void setCourseTime(String courseTime) {
		this.courseTime = courseTime;
	}
	public String getCourseMoney() {
		return courseMoney;
	}
	public void setCourseMoney(String courseMoney) {
		this.courseMoney = courseMoney;
	}
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getCourseNumber() {
		return courseNumber;
	}
	public void setCourseNumber(String courseNumber) {
		this.courseNumber = courseNumber;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	
	
	
	
	
}
